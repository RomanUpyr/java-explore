package ru.practicum.model;

/**
 * Статусы заявок на участие в событии.
 */
public enum RequestStatus {
    PENDING,    // В ожидании - заявка создана и ожидает подтверждения
    CONFIRMED,  // Подтверждено - заявка принята организатором
    REJECTED,   // Отклонено - заявка отклонена организатором
    CANCELED    // Отменено пользователем - заявка отменена пользователем
}
